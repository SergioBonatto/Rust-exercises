fn main(){
    let numero = 6;
    if numero % 4 == 0 {
        println!("o número é divisível por 4")
    } else if numero % 3 == 0 {
        println!("o número é divisível por 3")
    } else if numero % 2 == 0 {
        println!("o número é divisível por 2")
    } else {
        println!("o número não é divisível por 4, 3 e nem 2")
    }
}


